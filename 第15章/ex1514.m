%ex1514.m 解线性规划问题
%f(x)=-5x(1)+4x(2)+2x(3)
f=[-5,4,2]; %函数系数
A=[6,-1,1;1,2,4]; %不等式系数
b=[8;10]; %不等式右边常数项
l=[-1,0,0];  %下限
u=[3,2,inf]; %上限
%%%%用linprog求解
[xol,fol]=linprog(f,A,b,[],[],l,u)
%%%%用fmincon求解
x0=[0,0,0];
f1514=inline('-5*x(1)+4*x(2)+2*x(3)','x');
[xoc,foc]=fmincon(f1514,x0,A,b,[],[],l,u)

